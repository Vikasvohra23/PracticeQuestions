package com.glearning.practice.codingchallange_4;

public class SwapNumbers {
	public static void main(String[] args) {
		int Num1, Num2;
		Num1 = 9;
		Num2 = 7;
		System.out.println("Number 1 is " + Num1 + " & Number 2 is " + Num2);
		Num1 = Num1 + Num2;
		Num2 = Num1 - Num2;
		Num1 = Num1 - Num2;
		System.out.println("Number 1 is now " + Num1 + " & Number 2 is now " + Num2);
				
		
	}

}
