package com.glearning.practice.codingchallenge_4;

public class DemoSwitch {
	public static void main(String[]args) {
		int Num1 = 4;
		switch(Num1) {
		case 1: System.out.println("Number is Neither Prime Not Composite");
		break;
		case 2: System.out.println("Number is a Prime Number");
		break;
		case 4: System.out.println("Number is a Composite Number");
		break;
		
		}
	}

}


